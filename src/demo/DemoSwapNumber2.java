package demo;

public class DemoSwapNumber2 {

	public static void main(String[] args) {
int a = 20 ;
		
		int b = 40 ;
		
		// First we need one temp variable
		int c = a ;
		
		// 20 = 40 ;
		a = b ;
		
		// 40 = 20
		b = c ;
		
		System.out.println("Value of a = "+a);
		System.out.println("Value of b = "+b);
   

	}

}
